    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    String id = txtID.getText();
    String asientos = txtAsientos.getText();

    if (!editar) {
        cadenaSQL = "INSERT INTO asientos VALUES (?, ?)";
    } else {
        cadenaSQL = "UPDATE asientos SET asientos = ? WHERE id = ?";
    }

    try {
        PreparedStatement preparedStatement = controlador.retornarConexion().prepareStatement(cadenaSQL);

        if (!editar) {
            preparedStatement.setString(1, id);
            preparedStatement.setString(2, asientos);
        } else {
            preparedStatement.setString(1, asientos);
            preparedStatement.setString(2, id);
        }

        boolean operacion = preparedStatement.executeUpdate() > 0;
        System.err.println("Operacion: " + operacion);
        inicializar();
        tabAsientos.setSelectedIndex(1);
    } catch (SQLException ex) {
        System.err.println("Error al ejecutar la consulta: " + ex);
    };
    tabAsientos.setSelectedIndex(1);
    }//GEN-LAST:event_btnGuardarActionPerformed

